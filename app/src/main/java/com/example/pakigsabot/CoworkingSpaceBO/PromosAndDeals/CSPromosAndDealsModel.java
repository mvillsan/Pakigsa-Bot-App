package com.example.pakigsabot.CoworkingSpaceBO.PromosAndDeals;

public class CSPromosAndDealsModel {
    String coworkSpacePADId;
    String coworkSpacePADName;
    String coworkSpacePADDesc;
    String coworkSpacePADStartDate;
    String coworkSpacePADEndDate;

    public CSPromosAndDealsModel() {
    }

    public CSPromosAndDealsModel(String coworkSpacePADId, String coworkSpacePADName, String coworkSpacePADDesc, String coworkSpacePADStartDate, String coworkSpacePADEndDate) {
        this.coworkSpacePADId = coworkSpacePADId;
        this.coworkSpacePADName = coworkSpacePADName;
        this.coworkSpacePADDesc = coworkSpacePADDesc;
        this.coworkSpacePADStartDate = coworkSpacePADStartDate;
        this.coworkSpacePADEndDate = coworkSpacePADEndDate;
    }

    public String getCoworkSpacePADId() {
        return coworkSpacePADId;
    }

    public void setCoworkSpacePADId(String coworkSpacePADId) {
        this.coworkSpacePADId = coworkSpacePADId;
    }

    public String getCoworkSpacePADName() {
        return coworkSpacePADName;
    }

    public void setCoworkSpacePADName(String coworkSpacePADName) {
        this.coworkSpacePADName = coworkSpacePADName;
    }

    public String getCoworkSpacePADDesc() {
        return coworkSpacePADDesc;
    }

    public void setCoworkSpacePADDesc(String coworkSpacePADDesc) {
        this.coworkSpacePADDesc = coworkSpacePADDesc;
    }

    public String getCoworkSpacePADStartDate() {
        return coworkSpacePADStartDate;
    }

    public void setCoworkSpacePADStartDate(String coworkSpacePADStartDate) {
        this.coworkSpacePADStartDate = coworkSpacePADStartDate;
    }

    public String getCoworkSpacePADEndDate() {
        return coworkSpacePADEndDate;
    }

    public void setCoworkSpacePADEndDate(String coworkSpacePADEndDate) {
        this.coworkSpacePADEndDate = coworkSpacePADEndDate;
    }




}
