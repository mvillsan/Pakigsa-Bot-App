package com.example.pakigsabot.SpaBO.PromoAndDeals;

public class SpaPromoAndDealsModel {
    String spaPADId;
    String spaPADName;
    String spaPADDesc;
    String spaPADStartDate;
    String spaPADEndDate;

    public SpaPromoAndDealsModel() {
    }

    public String getSpaPADId() {
        return spaPADId;
    }

    public void setSpaPADId(String spaPADId) {
        this.spaPADId = spaPADId;
    }

    public String getSpaPADName() {
        return spaPADName;
    }

    public void setSpaPADName(String spaPADName) {
        this.spaPADName = spaPADName;
    }

    public String getSpaPADDesc() {
        return spaPADDesc;
    }

    public void setSpaPADDesc(String spaPADDesc) {
        this.spaPADDesc = spaPADDesc;
    }

    public String getSpaPADStartDate() {
        return spaPADStartDate;
    }

    public void setSpaPADStartDate(String spaPADStartDate) {
        this.spaPADStartDate = spaPADStartDate;
    }

    public String getSpaPADEndDate() {
        return spaPADEndDate;
    }

    public void setSpaPADEndDate(String spaPADEndDate) {
        this.spaPADEndDate = spaPADEndDate;
    }

    public SpaPromoAndDealsModel(String spaPADId, String spaPADName, String spaPADDesc, String spaPADStartDate, String spaPADEndDate) {
        this.spaPADId = spaPADId;
        this.spaPADName = spaPADName;
        this.spaPADDesc = spaPADDesc;
        this.spaPADStartDate = spaPADStartDate;
        this.spaPADEndDate = spaPADEndDate;
    }





}
